# Indexing (Accedemos al valor de una determinada posicion):
cadena = "Ejemplo"
print(cadena[0])

# Slicing (comenzamos de X posicion hasta el final):
cadena2 = "Ejemplo 2"
print(cadena2[3:len(cadena2)+1])

# Slicing con numeros negativos:
cadena3 = 'Ejemplo 3'
print(cadena3[:-4])

# Invertimos la cadena:
cadena4 = 'Ejemplo 4'
print(cadena4[::-1])


