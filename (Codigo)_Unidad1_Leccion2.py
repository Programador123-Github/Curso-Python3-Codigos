# Tipos de formas para escribir cadenas:
	# "Ejemplo"
	# 'Ejemplo'
	# "'Ejemplo'"
	# '"Ejemplo"'
	# """ "Ejemplo" """
	# ''' 'Ejemplo' '''

# Caracteres Especiales en las Cadenas:
	# \n 	: New Line
	# \t 	: TAB

# Prefijos de las cadenas:
	# r''	: Raw
	# f''   : Format

# Ejemplo de raw r'':
print(r"C:\nombre\\carpeta") # Intenta ahora sin el prefijo, para que veas el resultado.


# Ejemplo de format f'':
nombre = "Andre"
print(f"Hola {nombre}")

# Ejemplo de new line:
print("Primera linea\nSegunda Linea.")

# Ejemplo de tab:
print("Hola\tMundo")



